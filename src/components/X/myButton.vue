<template>
<GridLayout
    :class="bClass"
    @tap="$emit( 'tap' )"
    @longPress="$emit( 'longPress' )"
    @touch="buttonTouched"
    ref="button"
>

<!---------------------------------------------------------------------------------------->

    <GridLayout rows="*,auto,*"  >
        <Label row=1 :text="' ' + String.fromCharCode( '0x' + icon ) + ' '" />
    </GridLayout>

<!---------------------------------------------------------------------------------------->

</GridLayout>
</template>

// -- =====================================================================================

<script lang="ts">

// -- =====================================================================================

import { Vue, Component, Prop }         from "vue-property-decorator"

// -- =====================================================================================

@Component

// -- =====================================================================================

export default class myButton extends Vue {

// -- =====================================================================================

@Prop() icon: string;
@Prop() bClass: string;

// -- =====================================================================================

press_TO: NodeJS.Timeout | any;
buttonTouched ( args ) {

    switch ( args.action ) {

        case "down":
            args.object.className = this.bClass + " pressed";
            break;

        case "up":
        case "cancel":
            args.object.className = this.bClass; 
            break;

    }

}

// -- =====================================================================================

}

// -- =====================================================================================

</script>

// -- =====================================================================================

